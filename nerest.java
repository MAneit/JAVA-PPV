// Define a class named 'nerest'
public class nerest {
            // Define the main method 
    public static void main (String  args[]){
         // Use a nested for loop to generate pairs of 'i' and 'j' values
        for(int i=1;i<=2; i++ ) { // Outer loop (i from 1 to 2)
            for (int j=1; j<=2;j++){// Inner loop (j from 1 to 2)
                System.out.println(i+" "+j); // Print the current values of 'i' and 'j'
            }
        }
    }
}

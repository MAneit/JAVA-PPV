public class app{  
public static void main(String args[]){  
int x=10;  
System.out.println(x++);//10 (11) // Print and then increment (post-increment), so it prints 10, then becomes 11 
System.out.println(++x);//12 // Increment and then print (pre-increment), so it increments to 12 and then prints 12 
System.out.println(x--);//12 (11) // Print and then decrement (post-decrement), so it prints 12, then becomes 11  
System.out.println(--x);//10   // Decrement and then print (pre-decrement), so it decrements to 10 and then prints 10
 
}}  